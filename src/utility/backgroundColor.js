function backgroundColor(topic) {
    switch (topic) {
        case 'chile':
                return '#4d9de0';
        case 'Subreddit ChileFit':
                return '#ff8352';
        case 'RepublicadeChile':
                return '#7776bc';
        case 'IT Chile':
                return '#57c78b';
        case 'Santiago':
                return '#71d6ca';
        case 'asklatinamerica':
                return '#7776bc';
        default:
                return '#fa686a';
    }
}

export default backgroundColor;