function backgroundColor(topic) {
        switch (topic) {
                case 'chile':
                        return '#435eca';
                case 'Subreddit ChileFit':
                        return '#c24a4a';
                case 'RepublicadeChile':
                        return '#e9a75b';
                case 'IT Chile':
                        return '#5db2b9';
                case 'Santiago':
                        return '#4ead6c';
                case 'asklatinamerica':
                        return '#96578c';
                default:
                        return '#b1c042';
        }
}

export default backgroundColor;